numero1 = int(input('digite um numero'))

# if sigunifica se (condição)
# != diferente
#  and significa e
# or significa ou
if numero1 % 2 ==0 and numero1 != 0:
    print('é par')
 
# elif sigunifica se não se 
elif  numero1 % 2 ==1:
    print('é impar')
 
 # else sigunifica se não(Utima opção)   
else:
    print('o numero é zero')
    